<p style="text-align: justify; margin-right:20px;">

    Usaha mandiri walet adalah sebuah perusahaan yang bergerak di bidang Usaha peternakan Burung Walet yang berlokasi di Kabupaten Tabalong. Usaha Tersebut bertujuan melestarikan populasi Burung Walet yang bertebaran dengan cara Menyediakan sarana berupa gedung untuk tempat berkembang biak serta menjadikan hasil panen dari Sarang Burung Walet agar bisa dijadikan obat kesehatan atau di perjualkan kepada pembeli Sarang Burung Walet sehingga hasil penjualan bisa mendapatkan keuntungan.
    Usaha mandiri walet berdiri pada tahun 2013, pembangunan di kerjakan secara bertahap dari waktu ke waktu, selama proses pembangunan tersebut pernah mengalami jeda waktu pengerjaan di Tahun 2015 di karenakan dana pembangunan tidak mencukupi kemudian pembangunan selesai pada Tahun 2017. Setelah melewati proses pembangunan dalam jangka waktu yang panjang maka Usaha Mandiri Walet dapat beroperasi dengan baik.

